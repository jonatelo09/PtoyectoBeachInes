   <h1>Puerto Escondido</h1>
  <hr>

 	<p >Puerto Escondido tiene fama de ser un lugar tranquilo, en donde se puede gozar de una vivencia vacacional, alejada del ruido de las grandes ciudades.
  Sin embargo, el atractivo  de este lugar depende de sus maravillosas olas para el surf, lo cual  deriva  la realización de emocionantes competencias, cuyos participantes son de todas partes del mundo.

  La playa de Zicatela es un reflector en Puerto Escondido, en donde se conjugan todos los factores mencionados y por ello, es una garantía de diversión y emociones vibrantes. Y es que, en efecto, la mejor playa para surfistas en México y la tercera más relevante en el mundo entero, es el ámbito en donde se realizan importantes torneos y además, en donde se localizan algunos de los mejores lugares para gozar de mucha fiesta y baile en el corazón de Puerto Escondido.

  </p>


  <p>Puerto Escondido ofrece una gran variedad de actividades recreativas y marinas, espacios naturales ricos en flora y fauna como montañas, cascadas, aguas termales, ríos y lagunas y una entretenida vida nocturna</p>


